</p>
<p style="padding-left: 26pt;text-indent: 0pt;line-height: 15pt;text-align: left;">
	「委託先」という。）に委託することができる。
</p>
<p style="padding-top: 2pt;text-indent: 0pt;text-align: left;"><br /></p>
