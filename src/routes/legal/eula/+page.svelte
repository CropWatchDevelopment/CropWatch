<h1 style="text-indent: 0pt;line-height: 26pt;text-align: center;">サービス利用規約</h1>
<h2 style="padding-top: 11pt;text-indent: 0pt;text-align: center;">
	第 <span class="s1">1 </span>章 総則
</h2>
<h2 style="padding-top: 16pt;padding-left: 5pt;text-indent: 0pt;text-align: left;">
	第 <span class="s1">1.1 </span>条（本規約について）
</h2>
<p
	style="padding-top: 2pt;padding-left: 26pt;text-indent: 0pt;line-height: 123%;text-align: justify;"
>
	このサービス利用規約（以下「本規約」という。）は、合同会社クロップウォッチ（以下「当社」という。）が提供するデータの提供等に関するクラウドサービス（以下「本サービス」という。）を顧客が利用するにあたり遵守すべき事項等を定める。
</p>
<h2 style="padding-top: 13pt;padding-left: 5pt;text-indent: 0pt;text-align: left;">
	第 <span class="s1">1.2 </span>条（定義）
</h2>
<p style="padding-top: 2pt;padding-left: 26pt;text-indent: 0pt;text-align: left;">
	本規約においては、次の用語はそれぞれ次の意味で使用する。
</p>
<ol id="l1">
	<li data-list-text="(1)">
		<p
			style="padding-top: 2pt;padding-left: 48pt;text-indent: -22pt;line-height: 124%;text-align: justify;"
		>
			「管理責任者」とは、顧客を代理して顧客アカウントの管理並びに本サービス利用契約の変更及び解約等を行う権限を付与された顧客の役員又は従業員（顧客が個人の場合は当該個人）をいう。
		</p>
	</li>
	<li data-list-text="(2)">
		<p style="padding-left: 47pt;text-indent: -21pt;line-height: 14pt;text-align: justify;">
			「顧客」とは、本規約に同意して当社と本サービス利用契約を締結した事業者をいう。
		</p>
	</li>
	<li data-list-text="(3)">
		<p
			style="padding-top: 3pt;padding-left: 48pt;text-indent: -22pt;line-height: 125%;text-align: justify;"
		>
			「顧客アカウント」とは、顧客の管理責任者が作成した本サービスを利用するためのアカウントであり、本サービス利用契約の変更及び解約等を行うことができるアカウントをいう。
		</p>
	</li>
	<li data-list-text="(4)">
		<p style="padding-left: 47pt;text-indent: -21pt;line-height: 14pt;text-align: justify;">
			「顧客情報」とは、顧客が本サービスに登録する情報その他第 <span class="s2"
				>3.1
			</span>条に具体的に定める情報を
		</p>
		<p
			style="padding-top: 3pt;padding-left: 48pt;text-indent: 0pt;line-height: 123%;text-align: left;"
		>
			含む、本サービスの利用を通じて顧客から直接又は間接に当社に提供される全ての情報、並びにこれらの情報に加工又は分析等を加えた情報（ただし、本データを除く。）をいう。
		</p>
	</li>
	<li data-list-text="(5)">
		<p style="padding-left: 48pt;text-indent: -22pt;line-height: 124%;text-align: justify;">
			「知的財産権」とは、著作権（著作権法第 <span class="s2">27 </span>条及び第
			<span class="s2"
				>28
			</span>条に定める権利を含む。）、特許権、実用新案権、意匠権、商標権その他の知的財産権（これらを受ける権利及び出願中の権利を含む。）をいう。
		</p>
	</li>
	<li data-list-text="(6)">
		<p style="padding-left: 48pt;text-indent: -22pt;line-height: 125%;text-align: justify;">
			「匿名データ」とは、顧客情報及び本データに対して一定の処理（統計的な処理又は解析を含む。）を加え、顧客及び個人が特定又は識別されないよう加工した情報をいう。
		</p>
	</li>
	<li data-list-text="(7)">
		<p style="padding-left: 47pt;text-indent: -21pt;line-height: 14pt;text-align: justify;">
			「パスワード」とは、ログイン <span class="s2"
				>ID
			</span>と組み合わせて、本サービス上で顧客をその他の顧客と区別す
		</p>
		<p
			style="padding-top: 2pt;padding-left: 48pt;text-indent: 0pt;line-height: 123%;text-align: left;"
		>
			るために用いられる符号であり、顧客が所定の条件に従って自ら設定する半角英数字及び記号の組み合わせをいう。
		</p>
	</li>
	<li data-list-text="(8)">
		<p style="padding-left: 47pt;text-indent: -21pt;line-height: 14pt;text-align: left;">
			「本サービス利用契約」とは、顧客と当社との間の本サービスの利用に関する契約をいう。
		</p>
	</li>
	<li data-list-text="(9)">
		<p
			style="padding-top: 3pt;padding-left: 48pt;text-indent: -22pt;line-height: 125%;text-align: left;"
		>
			「本ソフトウェア」とは、本サービスのために当社が提供するソフトウェア（本デバイスに組み込まれるものを含む。）をいう。
		</p>
	</li>
	<li data-list-text="(10)">
		<p style="padding-left: 47pt;text-indent: -21pt;line-height: 14pt;text-align: left;">
			「本データ」とは、本サービスを通じて収集されるデータ及びこれらに加工又は分析等を加えた
		</p>
		<p style="padding-top: 3pt;padding-left: 48pt;text-indent: 0pt;text-align: left;">
			データをいう。
		</p>
	</li>
	<li data-list-text="(11)">
		<p
			style="padding-top: 2pt;padding-left: 48pt;text-indent: -22pt;line-height: 125%;text-align: left;"
		>
			「本デバイス」とは、本データの収集、送信等のために当社が提供し、又は顧客が準備する情報端末、センサ、通信機器その他のデバイスをいう。
		</p>
	</li>
	<li data-list-text="(12)">
		<p style="padding-left: 47pt;text-indent: -21pt;line-height: 14pt;text-align: left;">
			「ログイン <span class="s2">ID</span
			>」とは、本サービスの利用に際して各顧客をその他の顧客と区別するために用いら
		</p>
	</li>
</ol>
<p style="padding-top: 3pt;padding-left: 48pt;text-indent: 0pt;line-height: 123%;text-align: left;">
	れるメールアドレスその他の符号であり、顧客が自ら登録する半角英数字及び記号の組み合わせをいう。
</p>
<h2 style="padding-left: 5pt;text-indent: 0pt;line-height: 19pt;text-align: left;">
	第 <span class="s1">1.3 </span>条（本規約の適用・変更）
</h2>
<ol id="l2">
	<li data-list-text="1.">
		<p style="padding-top: 2pt;padding-left: 26pt;text-indent: -21pt;text-align: justify;">
			当社は、顧客に対して、本規約に記載する条件にて本サービス及び本ソフトウェアの利用を許諾する。
		</p>
	</li>
	<li data-list-text="2.">
		<p
			style="padding-top: 3pt;padding-left: 27pt;text-indent: -22pt;line-height: 123%;text-align: justify;"
		>
			本規約は、顧客と当社との間の本サービス利用契約に適用される。但し、当社は、本規約とは別に、当社が提供する個別の本サービス（以下「個別サービス」という。）の内容に応じて、当該個別サービスの利用条件（名称の如何を問わず、以下「個別条件」といいます。）を定め、当社ウェブサイト上で掲載等する場合があり、この場合、個別サービスの利用者は、個別条件に同意のうえ、個別サービスを利用するものとする。
		</p>
	</li>
	<li data-list-text="3.">
		<p style="padding-left: 27pt;text-indent: -22pt;line-height: 125%;text-align: justify;">
			前項の場合を含め、顧客及び当社が本サービス利用契約において本規約の条件と異なる条件を合意した場合は、当該本サービス利用契約の条件が本規約の条件に優先するものとする。
		</p>
	</li>
	<li data-list-text="4.">
		<p style="padding-left: 26pt;text-indent: -21pt;line-height: 14pt;text-align: justify;">
			当社は、いつでも本規約を変更することができるものとする。但し、本規約の変更を行うときは、その
		</p>
		<p
			style="padding-top: 3pt;padding-left: 27pt;text-indent: 0pt;line-height: 123%;text-align: justify;"
		>
			効力発生時期を定め、かつ、本規約を変更する旨及び変更後の本規約の内容並びにその効力発生時期を当社ウェブサイトを通じて公開する方法その他の適切な方法により顧客に周知するものとする。
		</p>
	</li>
	<li data-list-text="5.">
		<p style="padding-left: 27pt;text-indent: -22pt;line-height: 124%;text-align: justify;">
			顧客は、前項に基づき定める変更後の本規約の効力発生時期以降に本サービスの利用を継続した場合、変更後の本規約の内容に合意したものとみなされるものとし、かかる合意に基づいて本サービス利用契約の内容も変更されるものとする。
		</p>
	</li>
</ol>
<h2 style="padding-top: 13pt;padding-left: 5pt;text-indent: 0pt;text-align: left;">
	第 <span class="s1">1.4 </span>条（本サービス利用契約）
</h2>
<p
	style="padding-top: 2pt;padding-left: 26pt;text-indent: 0pt;line-height: 123%;text-align: justify;"
>
	顧客が当社所定の方法により本サービスの利用を申し込み、当社が当該申込みを承諾することにより本サービス利用契約を締結する。但し、当該顧客が過去に当社との間の契約に基づく義務に違反した等合理的な理由がある場合、当社は当該顧客との間の本サービス利用契約の締結を拒絶することができる。
</p>
<h2 style="padding-top: 13pt;padding-left: 5pt;text-indent: 0pt;text-align: left;">
	第 <span class="s1">1.5 </span>条（委託）
</h2>
<p style="padding-top: 2pt;padding-left: 26pt;text-indent: 0pt;line-height: 123%;text-align: left;">
